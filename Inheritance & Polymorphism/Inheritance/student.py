class Student:
    def setrollno(self,number):
        self.number=number
    def getrollno(self):
        return self.number
    def setname(self,name):
        self.name=name
    def getname(self):
        return self.name
    def setaddress(self,address):
        self.address=address
    def getaddress(self):
        return self.address
    def setmarks(self,marks):
        self.marks=marks
    def getmarks(self):
        return self.marks